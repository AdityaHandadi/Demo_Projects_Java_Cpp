    //populate Drop Downs
    private void populateMembers(List<Member> memberList){
        MemberDao memDao = new MemberDao();
        memberList = memDao.getListOfMembers(this.group);
        //StringBuilder strBuff = new StringBuilder("");
        String str[] = new String[memberList.size()+1];
        int idx = 0;
        //str[idx++] = " ----- ";
        for(Member member:memberList){
          str[idx] = member.getName();
          idx++;
        }
        
        spentByValDD.setModel(new javax.swing.DefaultComboBoxModel(str));
        str[idx] = "Everyone";
        
        //spentByValDD.setModel(new javax.swing.DefaultComboBoxModel(str));
        splitWithValDD.setModel(new javax.swing.DefaultComboBoxModel(str));

    }
    
    //populate TextField with existing expenses so far
    private void populateExpense(List<Expense> expenseList){
        ExpenseDao expDao = new ExpenseDao();
        expenseList = expDao.getListOfExpense(this.group);
        
        StringBuilder strBuff = new StringBuilder("");
        for(Expense expense:expenseList){
          strBuff.append(" "+expense.getSplitWith()+" Owes "+expense.getSpentBy()+" an Amount of $"+expense.getAmount());
          strBuff.append(System.getProperty("line.separator"));
        }
        
        allExpenses.setText(strBuff.toString());
    }
