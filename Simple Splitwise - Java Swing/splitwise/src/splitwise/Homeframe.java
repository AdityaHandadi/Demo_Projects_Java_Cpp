    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

        System.out.println("Homeframe Save ActionPerformed");
        Expense expense = new Expense();
        expense.setGroupName(this.group);
        expense.setSpentBy(spentByValDD.getSelectedItem().toString());
        Double amount_ = Double.parseDouble(amount.getText());
        
        String splitType_ = splitType.getSelectedItem().toString();
        
        expense.setSplitWith(splitWithValDD.getSelectedItem().toString());
        
        if(splitType_.equals("Split") && !expense.getSplitWith().equals("Everyone")){
            amount_ = amount_/2;
            expense.setAmount(amount_); //new DecimalFormat("$0.00").format(amount)
        }
        else{
            expense.setAmount(amount_);
        }
        
        
        
        ExpenseDao expenseDao = new ExpenseDao();
        expenseDao.addExpense(expense.getSpentBy(), expense.getAmount(), expense.getSplitWith(), group);
        
        JFrame frame = new Homeframe(group);
        frame.setTitle("SplitWise");
        frame.setVisible(true);
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
    }//GEN-LAST:event_saveActionPerformed

