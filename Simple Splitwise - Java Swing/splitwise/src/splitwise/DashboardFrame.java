    //for upper sector displaying amount due in a group
    private void populateIndividualExpense(String group){
        
        MemberDao memberDao = new MemberDao();
        List<Member> memberlist = memberDao.getListOfMembers(group);
        //Final it should look like this
        String[][] indivExpArr = new String[memberlist.size()][2];

        int idx = 0;
        ExpenseDao expDao = new ExpenseDao();
        
        //For each member in memberList
        for(Member member:memberlist){
            //Find all spentByTotal by Member in group = room9
            Double spentByTot = expDao.getSpentByTot(group,member.getName());
            //Find all owedBy by same Member in group = room9
            Double owedBy = expDao.getOwedByTot(group,member.getName());
            //Amount due + or - would be owedBy - spentBy
            Double amountDue = owedBy - spentByTot;
            indivAmt.put(member.getName(),amountDue);
            //For each member split/share if owedBy = everyone equally
            
        }
        
        //Foreveryone, find expense from Dao for owedBy everyone.
        List<Expense> memberList2 = expDao.getListOfSplitAllMem(group);
        for(Expense exp:memberList2){
            Double totalAmount = exp.getAmount();
            Double indivShare = totalAmount/memberlist.size();
            
            //add indivShare to existing amount for everyone including individual Share of the paid
            Iterator<String> it = indivAmt.keySet().iterator();
            
            while(it.hasNext()){
                String key = it.next();
                Double amtDue = indivAmt.get(key);
                amtDue = amtDue + indivShare;
                indivAmt.put(key, amtDue);
                
            }
            
        }
        
        for(Member member:memberlist){
            //populate member and his amount due in String array which is fixed j = [0] & j = [1]
            Double amountDue = indivAmt.get(member.getName());
            indivExpArr[idx][0] = member.getName();
            indivExpArr[idx++][1] = new DecimalFormat("$0.00").format(amountDue);
        }

        summaryExpTable.setModel(new javax.swing.table.DefaultTableModel(
        indivExpArr,
        new String [] {
            "Member Name", "Amount Owed"
        }
        ));

        jScrollPane1.setViewportView(summaryExpTable);
        
    }
    
