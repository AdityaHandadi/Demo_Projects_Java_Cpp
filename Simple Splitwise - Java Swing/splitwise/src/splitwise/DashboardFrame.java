    //Calculating the amount Due for Expenses only between members A & B
    private void findIndivExpenseButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findIndivExpenseButActionPerformed
        System.out.println("findIndivExpense Action Button Called");
        
        String paidBy = memberNames.getSelectedItem().toString();
        String owedBy = memberNames1.getSelectedItem().toString();
        
        
        ExpenseDao expenseDao = new ExpenseDao();
        
        //First finds all expense summary between A & B
        List<Expense> expList = expenseDao.getExpenseA_B(paidBy,owedBy,this.group);
        String[][] expArr = new String[expList.size()+1][4];
        int idx = 0;
        
        for(Expense exp:expList){
            expArr[idx][0] = exp.getSpentBy();
            expArr[idx][1] = new DecimalFormat("$0.00").format(exp.getAmount());
            expArr[idx][2] = exp.getSplitWith();
            if(exp.getAmount()<0){
                expArr[idx][3] =  exp.getSpentBy()+" Owes "+exp.getSplitWith();
            }
            else{
                expArr[idx][3] =  exp.getSplitWith()+" Owes "+exp.getSpentBy();
            } 
            
            idx++;
        }
        
        //calculate actual expense owed between two
        MemberDao memberDao = new MemberDao();
        List<Member> memberlist = memberDao.getListOfMembers(group);
        Double totalAmountDue = expenseDao.getAmountDue(paidBy,owedBy,this.group,memberlist.size());
        expArr[idx][0] = "Total";
        expArr[idx][1] = new DecimalFormat("$0.00").format(totalAmountDue);
        String compareAmt = new DecimalFormat("0.00").format(totalAmountDue);
        
        totalAmountDue = Double.parseDouble(compareAmt);
        if(totalAmountDue > 0.0){
            expArr[idx][3] = owedBy+" Owes "+paidBy;
        }
        else if(totalAmountDue < 0.0){
            expArr[idx][3] = paidBy+" Owes "+owedBy;
        }
        else{
            expArr[idx][3] = " Settled ";
        }
        
        
        individualExpTable = new javax.swing.JTable();

        individualExpTable.setModel(new javax.swing.table.DefaultTableModel(
            expArr,
            new String [] {
                "MemberName/Paid By", "Amount Owed", "Owed By", "Remark"
            }
        ));

        jScrollPane3.setViewportView(individualExpTable);
    }//GEN-LAST:event_findIndivExpenseButActionPerformed

    
    
    
